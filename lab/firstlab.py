def s(i,j):
    k = i+j
    k.sort(reverse = True)
    return k 
first = [10,8,7,5,2,1,9,0]
second = [1200,2200,100,2,4,429,241,1100]    
sortreverse = s(first,second)
print('Sorted:{}'.format(sortreverse))